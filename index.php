<script>
(function() {
  const welcomeScreen = document.getElementById("welcomeScreen");
  const glassText = welcomeScreen.querySelector(".glass-text");

  // Show welcome only once per session
  if (sessionStorage.getItem('welcomeShown') === 'true') {
    welcomeScreen.style.display = 'none';
    return;
  }

  const currentLang = window.currentLanguage || 'en';
  const translationPath = `translations/${currentLang}.json`;
  const translationKey = glassText.getAttribute('data-i18n');

  // Default text is already in HTML, so no immediate change needed

  // Load translation in background (non-blocking)
  fetch(translationPath).then(response => {
    if (!response.ok) throw new Error('Translation file not found');
    return response.json();
  }).then(translations => {
    const translatedText = translationKey
      .split('.')
      .reduce((obj, key) => obj && obj[key], translations);
    if (typeof translatedText === 'string' && translatedText.trim() !== '') {
      glassText.textContent = translatedText;
    }
  }).catch(() => {});

  // Quick fade out after 1 second
  setTimeout(() => {
    welcomeScreen.classList.add('fade-out');
    setTimeout(() => {
      welcomeScreen.style.display = 'none';
      sessionStorage.setItem('welcomeShown', 'true');
    }, 800);
  }, 1000);

  // Max timeout 2.5 seconds to hide screen
  setTimeout(() => {
    if (welcomeScreen.style.display !== 'none') {
      welcomeScreen.classList.add('fade-out');
      setTimeout(() => {
        welcomeScreen.style.display = 'none';
        sessionStorage.setItem('welcomeShown', 'true');
      }, 1000);
    }
  }, 3500);
})();
</script>

